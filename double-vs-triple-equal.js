//double equal == it's checking just the value not the type
const first = 0;
const second = false;
if (first == second) {
    console.log('Condition is true.')
}
else {
    console.log('Condition is false.')
}

//triple equal === it's checking strictly the value & the type also.
const third = 0;
const fourth = false;
if (third === fourth) {
    console.log('Condition is true.')
}
else {
    console.log('Condition is false.')
}